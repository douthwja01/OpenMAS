% solution      ~[ 121.9 ; 114.2 ; 93.6 ; 62.3 ; 41.3 ; 30.5 ]
  y = x;
  n = length(x);
  beta = typeadj( intval('2.249 2.166 2.083 2 1.918 1.835')/100 , typeof(x) );
