% Broyden
% approximation [ .6 ; 3 ]
% solution      [ .5 ; pi ]
  y = x;
  c1 = typeadj( 1 , typeof(x) );
  cpi = typeadj( midrad(3.14159265358979323,1e-16) , typeof(x) );
